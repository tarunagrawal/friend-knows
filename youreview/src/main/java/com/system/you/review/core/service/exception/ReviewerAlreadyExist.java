package com.system.you.review.core.service.exception;

public class ReviewerAlreadyExist extends ServiceException {

	public ReviewerAlreadyExist(String providerId) {
		super();
		this.providerId = providerId;
	}

	public ReviewerAlreadyExist(String providerId, String message, Throwable ex) {
		super(message, ex);
		this.providerId = providerId;
	}

	public String getProviderId() {
		return providerId;
	}

	private String providerId;

	private static final long serialVersionUID = 1L;

}
